#include "threadpool.h"
#include <stdlib.h>
#include <pthread.h>
#include <stdio.h>
#include <string.h>
#include <unistd.h>
// 任务结构体
typedef struct Task
{
    void (*function)(void* arg);
    void* arg;
}Task;



// 线程池结构体
struct ThreadPool
{
    // 任务队列
    Task* taskQ;
    int queueCapacity;  // 容量
    int queueSize;      // 当前任务个数
    int queueFront;     // 队头 -> 取数据
    int queueRear;      // 队尾 -> 放数据

    pthread_t managerID;    // 管理者线程ID
    pthread_t* threadIDs;   // 工作的线程ID
    int minNum;             // 最小线程数量
    int maxNum;             // 最大线程数量
    int busyNum;            // 忙的线程的个数
    int liveNum;            // 存活的线程的个数
    int exitNum;            // 要销毁的线程个数
    pthread_mutex_t mutexPool;  // 锁整个的线程池
    pthread_mutex_t mutexBusy;  // 锁busyNum变量
    pthread_cond_t notFull;     // 任务队列是不是满了
    pthread_cond_t notEmpty;    // 任务队列是不是空了

    int shutdown;           // 是不是要销毁线程池, 销毁为1, 不销毁为0
};

ThreadPool* threadPoolCreate(int min, int max, int queueSize)
{
    ThreadPool* pool = (ThreadPool*)malloc(sizeof(ThreadPool));
    do
    {
        //分配线程池内存
        if (pool == NULL)
        {
            printf("malloc threadpool fail...\n");
            break;
        }

        //分配工作线程ID的内存并初始化
        pool->threadIDs = (pthread_t*)malloc(sizeof(pthread_t) * max);
        if (pool->threadIDs == NULL)
        {
            printf("malloc threadIDs fail...\n");
            break;
        }
        memset(pool->threadIDs, -1, sizeof(pthread_t) * max);

        //初始化结构变量
        pool->minNum = min;
        pool->maxNum = max;
        pool->busyNum = 0;
        pool->liveNum = min;    //存活的线程个数开始和最小个数相等
        pool->exitNum = 0;

        //初始化互斥锁与条件变量
        if (pthread_mutex_init(&pool->mutexPool, NULL) != 0 ||
            pthread_mutex_init(&pool->mutexBusy, NULL) != 0 ||
            pthread_cond_init(&pool->notEmpty, NULL) != 0 ||
            pthread_cond_init(&pool->notFull, NULL) != 0)
        {
            printf("mutex or condition init fail...\n");
            break;
        }

        //初始化任务队列
        pool->taskQ = (Task*)malloc(sizeof(Task) * queueSize);
        pool->queueCapacity = queueSize;
        pool->queueSize = 0;
        pool->queueFront = 0;
        pool->queueRear = 0;

        //
        pool->shutdown = 0;

        // 创建管理者线程和最小数量的工作线程
        pthread_create(&pool->managerID, NULL, manager, pool);
        for (int i = 0; i < min; ++i)
        {
            pthread_create(&pool->threadIDs[i], NULL, worker, pool);
        }

        //全部初始化成功
        return pool;

    } while (0);

    //有东西初始化失败,释放所有资源
    if (pool && pool->threadIDs) free(pool->threadIDs);
    if (pool && pool->taskQ) free(pool->taskQ);
    if (pool) free(pool);

    return NULL;
}

int threadPoolDestroy(ThreadPool* pool)
{
    if (pool == NULL)
    {
        return -1;
    }

    // 关闭线程池
    pool->shutdown = 1;
    // 阻塞回收管理者线程
    pthread_join(pool->managerID, NULL);
    // 唤醒阻塞的消费者线程
    for (int i = 0; i < pool->liveNum; ++i)
    {
        pthread_cond_signal(&pool->notEmpty);
    }
    // 释放堆内存
    if (pool->taskQ)
    {
        free(pool->taskQ);
    }
    if (pool->threadIDs)
    {
        free(pool->threadIDs);
    }

    pthread_mutex_destroy(&pool->mutexPool);
    pthread_mutex_destroy(&pool->mutexBusy);
    pthread_cond_destroy(&pool->notEmpty);
    pthread_cond_destroy(&pool->notFull);

    free(pool);
    pool = NULL;

    return 0;
}

void threadPoolAdd(ThreadPool* pool, void(*func)(void*), void* arg)
{
    pthread_mutex_lock(&pool->mutexPool);
    // 判断工作队列是不是已经满了
    while (pool->queueSize == pool->queueCapacity && !pool->shutdown)
    {
        //阻塞生产者线程
        pthread_cond_wait(&pool->notFull, &pool->mutexPool);
    }
    //判断线程池是否被关闭
    if (pool->shutdown)
    {
        pthread_mutex_unlock(&pool->mutexPool);
        return;
    }

    // 添加任务
    pool->taskQ[pool->queueRear].function = func;
    pool->taskQ[pool->queueRear].arg = arg;
    pool->queueRear = (pool->queueRear + 1) % pool->queueCapacity;
    pool->queueSize++;

    //生产了东西后也需要唤醒阻塞的工作线程
    pthread_cond_signal(&pool->notEmpty);

    pthread_mutex_unlock(&pool->mutexPool);

}

int threadPoolBusyNum(ThreadPool* pool)
{
    pthread_mutex_lock(&pool->mutexBusy);
    int busyNum = pool->busyNum;
    pthread_mutex_unlock(&pool->mutexBusy);
    return busyNum;
}

int threadPoolAliveNum(ThreadPool* pool)
{
    pthread_mutex_lock(&pool->mutexPool);
    int aliveNum = pool->liveNum;
    pthread_mutex_unlock(&pool->mutexPool);
    return aliveNum;
}

void* worker(void* arg)
{
    //强制转换为线程池结构(只有一个线程池)
    ThreadPool* pool = (ThreadPool*)arg;

    //循环的读取任务队列
    while (1)
    {
        //加锁线程池
        pthread_mutex_lock(&pool->mutexPool);

        //判断任务队列是否为空
        while (pool->queueSize == 0 && !pool->shutdown)//循环是因为可能要销毁多个线程
        {
            // 阻塞工作线程(与下面的管理线程配合让线程自杀或生产者生产了任务后唤醒)
            pthread_cond_wait(&pool->notEmpty, &pool->mutexPool);

            /*--------------------------------------------------*/
            // 判断是不是要销毁线程(都是逻辑上的销毁)
            if (pool->exitNum > 0)
            {
                pool->exitNum--;
                if (pool->liveNum > pool->minNum)
                {
                    pool->liveNum--;
                    pthread_mutex_unlock(&pool->mutexPool);
                    threadExit(pool);
                }
            }
            /*--------------------------------------------------*/
        }


        // 判断线程池是否被关闭了
        if (pool->shutdown)
        {
            pthread_mutex_unlock(&pool->mutexPool);
            threadExit(pool);
        }

        //开始消费任务队列中的任务
        Task task;
        task.function = pool->taskQ[pool->queueFront].function;//取头部的任务
        task.arg = pool->taskQ[pool->queueFront].arg;

        // 移动头结点
        pool->queueFront = (pool->queueFront + 1) % pool->queueCapacity;
        pool->queueSize--;

        //消费了任务队列后再唤醒生产者线程
        pthread_cond_signal(&pool->notFull);

        //解锁线程池
        pthread_mutex_unlock(&pool->mutexPool);

        //增加忙的线程个数(因为会有其他工作线程,所以要加锁)
        printf("thread %ld start working...\n", pthread_self());
        pthread_mutex_lock(&pool->mutexBusy);
        pool->busyNum++;
        pthread_mutex_unlock(&pool->mutexBusy);

        //执行任务函数并释放内存
        task.function(task.arg);
        free(task.arg);
        task.arg = NULL;
        printf("thread %ld end working...\n", pthread_self());

        //减少忙的线程个数
        pthread_mutex_lock(&pool->mutexBusy);
        pool->busyNum--;
        pthread_mutex_unlock(&pool->mutexBusy);
    }
    return NULL;
}

void* manager(void* arg)
{
    //强制转换为线程池结构(只有一个线程池)
    ThreadPool* pool = (ThreadPool*)arg;

    //管理者线程一直干活
    while (!pool->shutdown)
    {
        //3秒检测一次
        sleep(3);

        //取线程池中任务的数量和当前线程的数量(要加锁)
        pthread_mutex_lock(&pool->mutexPool);
        int queueSize = pool->queueSize;
        int liveNum = pool->liveNum;
        pthread_mutex_unlock(&pool->mutexPool);

        // 取出忙的线程的数量(其实可以放上面,不过这样看起来好一点,因为busyNum经常改)
        pthread_mutex_lock(&pool->mutexBusy);
        int busyNum = pool->busyNum;
        pthread_mutex_unlock(&pool->mutexBusy);

        //满足条件则添加线程(任务的个数>存活的线程个数 && 存活的线程数<最大线程数)
        if (queueSize > liveNum && liveNum < pool->maxNum)
        {
            pthread_mutex_lock(&pool->mutexPool);
            //每次加2个线程
            int counter = 0;
            for (int i = 0; i < pool->maxNum && counter < 2 && pool->liveNum < pool->maxNum; i++)
            {
                if (pool->threadIDs[i] == -1)//寻找没有存储线程ID的
                {
                    //创建了一个工作线程
                    pthread_create(&pool->threadIDs[i], NULL, worker, pool);
                    pool->liveNum++;
                    counter++;
                }
            }
            pthread_mutex_unlock(&pool->mutexPool);
        }

        //满足条件则销毁线程(忙的线程*2 < 存活的线程数 && 存活的线程>最小线程数)
        //thread_mutex_lock(&pool->mutexBusy);
        if (pool->busyNum * 2 < pool->liveNum && liveNum > pool->minNum)
        {
            //pthread_mutex_unlock(&pool->mutexBusy);//我认为这里还是得加一下busyNum锁,因为工作线程里面也修改了busyNum的值
            //byd就是这里改了好久
            
            pthread_mutex_lock(&pool->mutexPool);
            pool->exitNum = 2;
            pthread_mutex_unlock(&pool->mutexPool);

            //让工作线程自杀(好好好)
            for (int i = 0; i < 2; ++i)
            {
                pthread_cond_signal(&pool->notEmpty);//唤醒上面被条件变量阻塞的工作线程
            }
        }
    }
    return NULL;
}

void threadExit(ThreadPool* pool)
{
    //获得当前线程的线程ID
    pthread_t tid = pthread_self();
    for (int i = 0; i < pool->maxNum; i++)
    {
        if (pool->threadIDs[i] == tid)
        {
            pool->threadIDs[i] = -1;
            printf("threadExit() called, %ld exiting...\n", tid);
            break;
        }
    }
    //退出当前线程
    pthread_exit(NULL);
}
